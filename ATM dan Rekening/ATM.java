public class ATM{
	String tglbuat, tglaktif, Password, noatm;
	public void setTglBuat(String tglbuat){
		this.tglbuat=tglbuat;
	}
	public String getTglBuat(){
		return this.tglbuat;
	}
	public void setTglAktif(String tglaktif){
		this.tglaktif=tglaktif;
	}
	public String getTglAktif(){
		return this.tglaktif;
	}
	public void setPassword(String Password){
		this.Password=Password;
	}
	public String getPassword(){
		return this.Password;
	}
	public void setNoAtm(String noatm){
		this.noatm=noatm;
	}
	public String getNoAtm(){
		return this.noatm;
	}
}